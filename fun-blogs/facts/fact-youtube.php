		<h1>Facts about Youtube</h1>
	<p class = "info">YouTube is the internet's second largest search engine and has over one billion active monthly users. Over 100 hours of video are uploaded to YouTube every minute and more content is uploaded in 60 days than all 3 major U.S. TV Networks generated in the last 60 years. It's fair to say YouTube is a pretty big deal, here's some mind bending facts about this internet giant.</p>

<p class = "info">The payment processor website PayPal had a huge impact on YouTubes success. YouTube was initially funded by the bonuses received following the eBay buyout of PayPal. If it weren't for PayPal, YouTube may very well not exist.</p>

<p class = "info">YouTube founders Chad Hurley, Steve Chen and Jawed Karim, say two major events inspired them to create YouTube. Firstly Karim had trouble finding online footage of Janet Jackson's Super Bowl wardrobe malfunction. And later, Chen and Hurley had difficulty sharing a video shot at a dinner party in San Francisco in 2005.</p>

<p class = "info">YouTube actually started out life as a dating website. It began as a video dating site called "Tune In Hook Up", inspired by the site Hot or Not. Users were allowed to rate the attractiveness of potential partners based on their uploaded videos. But the idea didn't catch on.</p>

<p class = "info">YouTube caused major problems for the Universal Tube & Rollform Equipment company based in Ohio. The company's domain was utube.com, upon the launch of the video sharing website, utube.com was overwhelmed with visitors attempting to spell the video sharing website's address phonetically. Utube.com actually sued the video sharing website, claiming their business was hurt. but the case failed and utube changed its website address to utubeonline.com</p>

<p class = "info">In 2007 YouTube used as much bandwidth as the whole Internet did in 2000.</p>

<p class = "info">In 2006 shortly before Google purchased YouTube for $1.65 billion, YouTube was losing money at a rate of $500,000 a month, despite it's immense popularity. Google however turned that around when they saw its huge advertising potential.</p>

<p class = "info">During it's early days, in an attempt to increase popularity, YouTube offered $100 to attractive girls who posted 10 or more videos on their site. The ad was posted on Craigslist, but surprisingly the YouTube founders didn't receive a single response.</p>

<p class = "info">The first ever video uploaded to YouTube was called "Me at the Zoo" and shows YouTube cofounder, Jawed Karim, talking about the long trunks of elephants. It has since got over 16 million views. The very first words uttered on YouTube were "All right, so here we are in front of the elephants."
</p>
<p class = "info">Just 30% of videos on YouTube make up 99% of views on the site. Only 0.33% of videos have over 1 million views, on the other hand, 70% of videos have less than 500 views.</p>

<p class = "info">YouTube has quite an impressive reach globally. More than 80% of YouTube traffic comes from outside the US. And YouTube is localised in 61 countries and across 61 languages.</p>

<p class = "info">Every second there are an average of 46,296 YouTube videos being viewed all around the world.</p>

<p class = "info">YouTube has over 800 million unique monthly visitors. That's more than the entire population of Europe.</p>

<p class = "info">YouTube has empowered freedom of speech in many countries, but it has also been blocked in four countries, China, Iran, Pakistan and Turkmenistan.</p>

<p class = "info">And finally, YouTube's running cost is estimated to be around $6 million a month.</p>
