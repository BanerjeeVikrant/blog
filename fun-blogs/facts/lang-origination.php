		<h1>How did languages originated?</h1>
	<p class = "info">There are estimated to be around 6,500 spoken languages in the world today. But more than half the world's population uses only 20 of them and a language dies out every 14 days. The most spoken language is Mandarin, with 955 million native speakers, followed by Spanish with 470 million and then English with 360 million. But how did language originate? And why don't we all speak the same language? Let's find out.</p>

<p class = "info">Homo sapiens have been on earth for around 7 million years, and modern humans as we know them today have been around for 200,000 years. The human race is ancient, but we've only been able to communicate with each other via language for a, rather short, 100,000 years. Scientists believe that humans have always possessed the building blocks for language, but it wasn't until 100,000 years ago when a chance mutation occurred in a single human that put the final building block in place inside the brain, which enabled humans to finally be able to talk using legible words.</p>

<p class = "info">It's thought that the very first words were emulations of noises that would have been heard in nature, such as the noises of animals, and the wind blowing through the trees. Scientists theorise that the very first words spoken by humans could very well have sounded like these: Bow-wow, Pooh-pooh, Ding-dong, Yo-he-ho and Ta-ta.</p>

<p class = "info">But the truth is we know very little about primitive languages because writing wasn't developed until much later, and without writing we have almost no evidence of ancient languages. The first written language we have found evidence for is ancient Egyptian. We have found Egyptian hieroglyphs dating back as far as 2690 BC.</p>

<p class = "info">But why do we have different languages, wouldn't it be much simpler if we all spoke the same language? To understand this we first need to understand how languages develop. thousands of years ago, isolated civilisations across the globe all had the same problem, they needed a way to describe the things they owned, the tools they used and the things they did, to their fellow citizens. So they came up with words for all their tools, possessions and actions, these names would start with one person calling an object by a made up word, and after referring to it as that word for some time. Other people would begin calling it by the same name, and eventually, the word would stick. This enabled them to work together more efficiently and get more done.</p>

<p class = "info">Today we can get from one side of the planet to the other in just a few hours, but back then travel took so long that most people didn't bother. This meant most groups of people where in isolation from the rest of the world, this led to thousands of different groups of people coming up with thousands of different words for various objects. That's why some people may call this an apple, and other would call it a pomme. Over time these isolated groups of people met with other isolated groups of people, when this happened they needed a common way to describe objects, so they both adapted their self taught languages to create a new language that was a mix of the both. This happened time and time again, and after enough groups of people mixed their languages together, a new super-language was created, which was a combination of all the sub groups. This would eventually grow to become one of the many languages we know today, such as English or French.</p>

