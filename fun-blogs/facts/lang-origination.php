<p class = "info">However in the modern day where everyone is so well connected and we can communicate with people on the other side of the world in seconds, wouldn't it be beneficial if we all spoke the same language. Many predict that a single global language would significantly increase collaboration and productivity, and some groups have actually pushed for a universal language, but all attempts to do so have so far failed. The problem is, that if the world were to adopt a universal language, who would have to give up their native tongue and who gets to keep it, it's not an easy decision. Language is just too deeply embedded within our cultures to just give it up. To do so would be surrendering your countries culture and history. So, for the foreseeable future, languages as we know them today are here to stay.</p>

<p class = "info">But language does change, and quite rapidly in fact. In the short time we've had the internet the English language has already changed quite considerably. In March 2011 the Oxford English Dictionary officially recognised "LOL" as a word. But what will the English language look like in 1000 years from now. Well Justin B Rye challenged this question, taking all the effects of different cultures and technology into consideration. He proposed a new language, Futurese.</p>

