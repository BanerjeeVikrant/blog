		<p class = "info">The word Halloween is derived from "All Hallows' Eve". All Hallows' Eve is the eve before the Christian feast of All Hallows on November 1st.</p>
<hr>
<p class = "info">Halloween takes place on the 31st October, mostly in the western world. More chocolate and sweets are sold on October 28th than any other day of the year. The most popular of which is Snickers.</p>
<hr>
<p class = "info">Halloween is the second highest grossing commercial holiday after Christmas. Americans spend $6 billion on Halloween every year.
<br>
But why do we celebrate Halloween?</p>
<hr>
<p class = "info">Halloween started about 2000 years ago in Ireland. The holiday originated from the Celtic festival of Samhain. Samhain was celebrated by Celts in Ireland, the UK and northern France. The festival was held to mark the end of the summer months and harvest time. The darker, colder months that followed were associated with death by the Celts. Samhain began at sunset on October 31st. They believed this was the time when the ghosts of the deceased returned to earth, because the boundaries between the world of the living and the dead were most blurred.</p>
<hr>
<p class = "info">So why do we call it Halloween today? The word Halloween comes from the Christians. Hallowmas is a three day Catholic holiday where saints are honoured and people pray for the recently deceased. At the start of the 11th century, the Pope decided that it would last from October 31st until November 2nd because that's when Samhain was celebrated and the church was trying to convert the pagans. October 31st, the start of this Christian holiday is known as All Hallows' Eve, which over the centuries has been shortened, first to All Hallow's Even and eventually in the 18th century to Hallowe'en.</p>
<hr>
<p class = "info">The Irish brought their Halloween-like traditions to the US in 1840. However the commercialised Halloween that we know today didn't start in the western world until 1921. And mass production of Halloween costumes didn't start until the 1950s, until then Halloween costumes were homemade and just plain creepy. But why do we dress up for Halloween in the first place?</p>
<hr>
<p class = "info">During the old festival of Samhain spirits were believed to cross over to the world of the living for this one night. People began to get scared at the thought of this so they wore handmade masks and odd clothing in an attempt to disguise themselves from the spirits so any evil spirits wouldn't harm them.</p>
<hr>
<p class = "info">Trick or treating is a tradition carried out across most of Europe, North America and some parts of Asia. But the ritual of dressing up and knocking on strangers doors in exchange for sweets differs greatly in some parts of the world.</p>
<hr>
<p class = "info">In France Halloween is seen as an unwanted American influence and the French rarely trick or treat. In fact the French didn't fully celebrate the holiday until 1996, because of the French Catholic Church's strong campaign against Halloween. Instead of going house to house asking for sweets, French children go from store to store.</p>
<hr>
<p class = "info">In Germany it is customary to put away all their knives, because they don't want the returning spirits to harm themselves.</p>
<hr>
<p class = "info">In Sweden Halloween is known as "Alla Helgons Dag" and is celebrated for 6 continuous days, school children are also given a day off.</p>
<hr>
<p class = "info">In Spanish speaking countries such as Mexico, Spain and Latin America, Halloween is known as "El Dia de los Muertos", The Days of the Dead. Families remember their dead relatives by constructing an altar in their homes, consisting of flowers, candy, photographs, water and samples of the deceased favourite food and drinks.</p>
<hr>
<p class = "info">But how did the tradition of trick or treating originate?</p>

<p class = "info">It all started in England, across the West Midlands and Lancashire, in the early 19th century, when children would go "souling". Kids would go door-to-door, singing songs and begging for money, food and drink. They would sing songs such as:</p>

<pre>
A soul! a soul! a soul-cake!

Please good Missis, a soul-cake!

An apple, a pear, a plum, or a cherry,

Any good thing to make us all merry.

One for Peter, two for Paul

Three for Him who made us all.
</pre>
<hr>
<p class = "info">The most common treat given out were Soul Cakes, they were small round cakes with a cross on top.</p>
<hr>
<p class = "info">Later in 19th century Britain, souling gave rise to guising. Where children would dress up and beg for fruit and money, but instead of singing songs to earn these gifts, children would put on full blown performances such as playing instruments and reciting poems. This tradition eventually gave way to modern day Trick or Treating when the Scottish and Irish brought it to North America in 1911.</p>
<hr>
<p class = "info">But is trick or treating really safe? What about all those poisoned candy scares and worried mummies? Well you can relax, truth is you're more likely to be ravaged by zombies that receive tampered treats. There have only ever been two documented cases of people dying as a result of eating Halloween goodies. The first was in 1970 when a boy ate a large amount of heroin that his uncle had hidden. The boy's family sprinkled heroin on his halloween sweets and blamed the sweets instead. The second was when a father poisoned his own son's sweets to get the compensation from a life insurance policy he had just taken out on him. So what's the lesson here? You should probably be more scared of your parents than strangers.</p>
<hr>
<p class = "info">And finally, why do we carve pumpkins? In 19th century Great Britain, jack-o-lanterns where a form of pranksterism, young boys used to hollow out and carve faces in turnips to frighten people on the streets. Wait, what, turnips? That's right before pumpkins, jack-o-lanterns were created from turnips. And the word "jack-o-lantern", well it comes from 17th century Britain, when it literally meant "man with a lantern".</p>

