	<h1>Can a Opera Singer shatter a wine glass?</h1>
		<p class = "info">Have you ever done this? The sound being emitted from the glass is what's known as its resonant frequency. That's the the pitch at which an object emits sound when affected by a physical object. Every object has a different resonant frequency. It's easier to hear resonance from a wine glass because its unique bowl shape amplifies the sound.</p>
		
<hr>

<p class = "info">So how can an opera singer use this knowledge to shatter the glass? Sound is essentially the compression and rarefaction of the air around the source, creating pressure waves, that's why sound can't be heard in a complete vacuum, because there's no air molecules to affect. When you sing you create these pressure waves on the air around you. However, if you sing at the exact same pitch or frequency that the wine glass resonates at, you will start to create pressure waves around the glass, and if you sing loud enough and long enough you can shatter the glass. But it will require a lot of lung power, and since Opera singers excel in this field, they're the most likely to shatter glass with their voice.</p>

<hr>

<p class = "info">Okay, so you can obliterate objects with your voice, that's impressive, but you can do some even more amazing things with sound. Sound can actually be weaponised, the Long Range Acoustic Device, is a "sound spotlight" capable of emitting a beam of sound at a target hundreds of metres away, up to 150 decibels. It's used for crowd control, such as breaking up riots. However they're also used on European cruise ships to ward off Somali pirates.</p>

<hr>

<p class = "info">Sound has also been weaponised on a smaller scale to take on all those stabby teenagers running amok. The Mosquito is an anti anti-social behaviour device. This small speaker can be attached to the front of shops, railway lines and anywhere else you don't want those little rascals loitering about. The speaker emits an annoying high pitched noise at 17KHz. Because our hearing degrades with age, the Mosquito's high frequency sound waves can only be heard by teenagers, thus, keeping them well clear of your property.</p>

<hr>

<p class = "info">A similar, yet less obnoxious approach to fighting minor crime was taken in London. When people hear music they don't like, their dopamine production is suppressed. In 2003 subway stations in London started to play classical music and it worked, within a year and a half, robberies and vandalism dropped by a third.</p>

<hr>

<p class = "info">But how dangerous can sound get? Well, theoretically it's possible to kill someone with sound, albeit rather indirectly. When a bomb explodes the sudden rise in atmospheric pressure creates what's called blast overpressure, resulting in an incredibly loud noise being emitted over a large area, you would't hear the noise because your eardrums shatter at 160 decibels. But, at 200 decibels the sound pressure would be enough to rupture a lung and cause fatal internal injuries. This caused confusion in World War 1, when soldiers were found dead despite having no external injuries. This means that all those films you've seen where the protagonist get's thrown from a massive explosion and survives are complete crap, the sound alone would kill them.</p>

<hr>

<p class = "info">However, when controlled, sound pressure can be used to heal people. A High-Intensity Focused Ultrasound Transducer is a device that can focus sound energy to such intense levels that it creates heat. Similar to how kids burn ants with a magnifying glass. The device can heat an area as small as a grain of rice to hundreds of degrees. In one study it was able to seal the punctured lungs of pigs in just 2 minutes. This new technology means that surgery could be performed without making any incisions on the body or using general anaesthesia, and patients will be able to go home the same day. Furthermore it can be used to cure cancer, by using the HiFU device to heat up and kill the cancer cells. The technology was tested on 159 men with prostate cancer, and after one year 92% of them did not have recurrence cancer.</p>

<hr>

<p class = "info">Surprisingly you can actually eliminate noise by adding more noise. Noise cancellation headphones work on this exact premise, they have a tiny microphone on their periphery that constantly monitors all noise outside your headphones. Usually this noise will have a distant waveform of peaks and troughs, the earphones internal circuitry create a new waveform that has peaks and throughs exactly opposite to the recorded noise and plays it into your ear, this is called an anti phase. The antiphase cancels out the background noise and all you hear is your music. It may seem like a strange concept, that adding more noise can remove it completely. But it's like having -2 and adding 2, you end up with nothing.</p>

<hr>

<p class = "info">A company has designed a system that uses this technology in restaurants and bars to cancel out the noisy background drone of customers. It uses an array of over 100 microphones and speakers concealed behind paintings around the venue. The microphones record the noise of the customers and creates an antiphase which is then played back to the customers via the speakers, cancelling out the ambient noise. This let's people converse with each other without having to raise their voices. But if the venue is mostly empty it can also pump background noise out of the speakers to make the place seem busier than it actually is. However the entire system can cost up to $100,000 to install.</p>

<hr>

<p class = "info">Can you guess where the quietest place on earth is? It's a small room in Salford, Manchester, England, more specifically the anechoic chamber at the University of Salford. It's actually a room within a room, steel and crosshatched buffers that absorb all sound. The room absorbs 99.9% of all sound, the room's sound level has been measured at -9.4 decibels, it's so quiet that you can hear your own internal organs, and the blood pumping through your veins. The chamber is used for testing low level noise, such as the hum of a light bulb. But ironically most people find the place upsetting.</p>

<hr>

<p class = "info">No matter where you are there's always a considerate amount of background noise and our brain's are very accustomed to it. The presence of noise tells our body that things are working. However, if you remove all sound then all your senses heighten and your body goes into panic mode. On 9/11 despite having no mobile phone reception, a huge number of hikers mysteriously abandoned their walks. They hadn't heard about the terrorist attacks, their brain's were just spooked by the lack of aeroplane noise and they knew something was wrong.</p>

<hr>

<p class = "info">After 5 minutes in the chamber you start to notice the smallest sounds such as your scalp moving across your skull when you frown and your heart pounding in your chest. Staying in the chamber for longer than 15 minutes can cause extreme symptoms, such as nausea and panic attacks. Most people suffer aural hallucinations, this is when you start hearing sounds that aren't even there, everything from scraping metal to voices. Apparently nobody can survive 45 minutes in the room without going insane.</p>

<hr>

<p class = "info">But for some unfortunate individuals such surreal experiences are an everyday occurrence. Superior canal dehiscence syndrome is a rare medical condition, affecting the inner ear. It amplifies all internal sounds. Sufferers hear their own heartbeat, creaking joints and other unsavoury internal bodily functions. It can get so bad that the sound of the eyeballs moving in their sockets sounds like "sandpaper on wood".</p>

<hr>

<p class = "info">Sound travels at 761.2 mph at sea level. But what happens when you travel faster than the speed of sound? You experience what's called a sonic boom, which is an extremely loud thumping sound, it also creates a visual phenomenon called a mach cone. This happens because when an object travels through the air it creates a series of pressure waves in front and behind it, similar to how a boat displaces the water. The faster the object travels the more the pressure waves are compressed together, because they can't get out of the way of each other fast enough. Once you reach 761 mph, otherwise known as Mach 1, the pressure you've created reaches a critical mass, creating a shock wave. But you won't hear anything at all, because you're travelling faster than the speed of sound, the sound will never reach your ears, unless you slow down. But everyone on the ground will hear.</p>
