		<h1>Story of Ramesh Babu</h1>
		<p class = "info main">
            Ramesh Babu is a Bangalore-based barber who gives customers a haircut for a modest Rs 100 but owns a Rolls Royce Ghost worth Rs 3 crore, and 67 other cars. When his father died in 1979, Ramesh, a nine year-old boy, was literally on the streets. The barber shop his father ran on Brigade Road's St Patrick's Complex was rented out for Rs 5 per day. Ramesh's family survived on that money. Babu was nine when his father died in 1979, leaving the family with nothing. To make ends meet, they rented out his father's saloon on Brigade Road for a mere Rs 5 per day. Forced to get a fulltime job, Babu dropped out of school after Class 10 and started working as a barber. The career choice paid off better than he could have imagined. In 1991, he started his own salon, Inner Space, which gained popularity among the city's trendy bunch. But the turnaround happened in 1994, when Babu purchased a humble Maruti Omni van and started renting it out. Seventeen years later, his car collection is anything but humble. Apart from the Rolls Royce, his fleet includes four Mercedes, an equal number of BMWs and 50 Toyota Innovas
        </p>
		<p class = "info">Leonard Willoughby said, "As you begin to live according to your own guidance and 
        your own daring everything changes completely." Ramesh Babu, the barber who became a 
        millionaire, did exactly this when he was shaping his dazzling destiny. Stories of 
        personal perseverance, the ones where heroes overcome severe obstacles and achieve 
        dizzying heights of success, have been around since the beginning of time but they 
        never get old. They inspire us and inflame our passions, making us believe we too can
        follow suit. Ramesh Babu bought a Maruti Van with his meagre savings in 1994. 
        By 2004, he had a fledgling car rental business with seven regular cars. In 2014 
        he has a fleet of 200 cars. What is even more extraordinary is the 75 luxury cars 
        on the fleet- a range of Mercedes, BMW's, Audi's, five and ten seater luxury vans 
        and, his ultimate pride, a Rolls Royce.
        </p>
		<p class = "info">
            Much of Ramesh Babu's early life was spent in a struggle for survival. 
            Now, ensconced in the lap of success, he remains true to the vocation of
            his heart- a barber. Babu took up to sniping locks in high school, a profession
            he inherited from his father, to keep his family afloat. It is one that, even now,
            he does with great aplomb. He charges only hundred rupees for his services. He has
            been featured in television channels and newspapers all over the country. His 
            phenomenal success coupled with his disarming humility has earned him the moniker,
            'Millionaire Barber'. It is the title he used while giving a TED talk earlier this year.
            This has made him something of an urban legend. Here Ramesh Babu, the Millionaire Barber,
            shares his utterly inspiring story.
        </p>
		<h2 class = "side-heading">Breaking Point</h2>
        <p class = "info">Sometime in the nineties, when I was in my first PU, my mother had a bitter fight with my uncle. He had simply stopped paying us any money. I told her I should take over the saloon and run it myself. She was adamant that I prioritise my education, but I started working at the saloon too and learning the ropes of the business. In the mornings, I would be at the saloon and evenings at college. Then again at night I would return to the saloon, which would remain open till 1 in the morning. Since then I have been called a barber.</p>
		
		<h2 class = "side-heading">Breakthrough Idea</h2>
        <p class = "info">Later in 1993, I bought a used Maruti van. My uncle had bought a small car and petty pride made me buy one too. I pooled my tiny savings, took a loan and felt grim satisfaction at having bought a marginally grander vehicle than him. My grandfather had to mortgage his property to enable the loan. The loan interest was six thousand and eight hundred rupees and I was reeling from having to make the payments.</p>
        <p class = "info">The lady whose house my mother used to work, Nandini akka as I like to call her, asked me why I don’t rent out the car instead of it just lying around. She taught me the basics of doing this kind of business. She became like a sister to me and remains a big part of my life even today. She called me to her daughter’s wedding and showed me off!</p>
		
		<h2 class = "side-heading">Building a successful business:</h2>
        <p class = "info">From 1994 onward I seriously got into the car rental business. The first company I rented it out to was Intel because that’s where Nandini akka was working and she helped arrange it. Gradually, I started adding more cars to the fleet. Till 2004, I only had about five to six cars. I was focused on getting the saloon business off the ground, so this was not my priority. The business was not doing well as the competition at this level was intense. Everyone had small cars. I thought of getting into luxury cars because that is something that no one else was doing.</p>
		
		<h2 class = "side-heading">On Taking Risks:</h2>
        <p class = "info">When I was buying my first luxury car, in 2004, everyone told me that I was making a big mistake. Forty lakhs in 2004 for a car, even a luxury car, was a very big deal. I was extremely apprehensive, but simply had to take the chance. I told myself that I would sell off the car if worse came to worst. Fortunately for me, the risk paid off remarkably. No other car rental service had luxury cars of this stature. There were ones who had purchased second hand models and the conditions of those cars were far from pristine. I was the first person in Bangalore to invest in a brand new luxury car and it did very well.</p>
    
        <p class = "info">If you want to do business, you must be willing to take risks. When I bought my Rolls Royce in 2011, people warned me about the scope of failures against buying such a tremendously expensive car. I told myself that I had taken a risk in 2004, why can’t I take one again almost a decade later? It cost me almost four crore rupees, to buy the car. But once again, it was a risk that paid off. It’s been three years since, and it’s has proven to be tremendously popular. In December this year, the EMI payments will be over.</p>
    
